def _external_index():
    for c in (os.path.join(BASE_DIR, "web", "index.html"),
              os.path.join(BASE_DIR, "index.html")):
        if os.path.exists(c):
            return c
    return None


@app.route("/")
def home():
    ext = _external_index()
    if ext:
        resp = send_file(ext)
    else:
        resp = Response(EMBEDDED_HTML, mimetype="text/html")
    # Force the browser to always fetch the latest dashboard (no stale cache).
    resp.headers["Cache-Control"] = "no-store, no-cache, must-revalidate, max-age=0"
    resp.headers["Pragma"] = "no-cache"
    resp.headers["Expires"] = "0"
    return resp


